CREATE TABLE IF NOT EXISTS "feeding_plans" (
	"id"	INTEGER,
	"device_id"	INTEGER NOT NULL,
	"name"	TEXT NOT NULL,
	"time_of_day"	TEXT NOT NULL,
	"days_of_week"	TEXT NOT NULL,
